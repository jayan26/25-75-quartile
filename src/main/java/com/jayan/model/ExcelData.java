package com.jayan.model;

public class ExcelData {

	private double comp1;
	private double comp2;
	private double comp3;
	private double comp4;
	private double comp5;
	
	public ExcelData() { }

	public ExcelData(double comp1, double comp2, double comp3, double comp4, double comp5) {
		this.comp1 = comp1;
		this.comp2 = comp2;
		this.comp3 = comp3;
		this.comp4 = comp4;
		this.comp5 = comp5;
	}

	public double getComp1() {
		return comp1;
	}

	public void setComp1(double comp1) {
		this.comp1 = comp1;
	}

	public double getComp2() {
		return comp2;
	}

	public void setComp2(double comp2) {
		this.comp2 = comp2;
	}

	public double getComp3() {
		return comp3;
	}

	public void setComp3(double comp3) {
		this.comp3 = comp3;
	}

	public double getComp4() {
		return comp4;
	}

	public void setComp4(double comp4) {
		this.comp4 = comp4;
	}

	public double getComp5() {
		return comp5;
	}

	public void setComp5(double comp5) {
		this.comp5 = comp5;
	}

}
